package lacombech.Jour3.Jour3Tableau;

public class Probleme1d3t {
    public static void main(String[] args) {
        String[] mots = new String[5];
        mots[0] = "Pomme";
        mots[1] = "Banane";
        mots[2] = "orange";
        mots[3] = "fraise";
        mots[4] = "bleuet";

        System.out.println("Les mots dans le tableau sont :");
        for (String mot : mots) {
            System.out.println(mot);
        }
    }
}
